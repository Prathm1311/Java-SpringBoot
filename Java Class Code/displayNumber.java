public class displayNumber {
    public static void main(String[] args) {
        //decleared and assined number 
        int num = 100; 
        //Simple Printed number on console  
        System.out.println("Added 20 to original:      "+ (num + 20));
        System.out.println("Subtracted 20 to original: "+ (num - 20));
        System.out.println("Multiplyed 20 to original: "+ (num * 20));
        System.out.println("Divided by 20 to original: "+ (num / 20));
    }
}
